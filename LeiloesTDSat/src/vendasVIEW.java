    private void listarProdutosVendidos() {
        try {
            ProdutosDAO produtosdao = new ProdutosDAO();

            DefaultTableModel model = (DefaultTableModel) listaProdutosVendidos.getModel();
            model.setRowCount(0); // Limpar a tabela antes de adicionar novos dados

            ArrayList<ProdutosDTO> listagemVendidos = produtosdao.listarProdutosVendidos();

            for (ProdutosDTO produto : listagemVendidos) {
                model.addRow(new Object[]{
                    produto.getId(),
                    produto.getNome(),
                    produto.getValor(),
                    produto.getStatus()

                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao listar produtos vendidos: " + e.getMessage());
        }
    }

